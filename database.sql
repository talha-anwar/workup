CREATE DATABASE IF NOT EXISTS workup
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

USE workup;

CREATE TABLE users (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(150) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    role ENUM('client', 'freelancer', 'both', 'admin') NOT NULL,
    status ENUM('active', 'suspended', 'banned') NOT NULL DEFAULT 'active',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    UNIQUE KEY uq_users_email (email)
);

CREATE TABLE freelancers (
    user_id INT UNSIGNED NOT NULL,
    bio TEXT,
    hourly_rate DECIMAL(8,2) NOT NULL DEFAULT 0.00,
    PRIMARY KEY (user_id),
    CONSTRAINT fk_freelancers_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

CREATE TABLE skills (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    name VARCHAR(50) NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uq_skills_name (name)
);

CREATE TABLE freelancer_skills (
    freelancer_id INT UNSIGNED NOT NULL,
    skill_id INT UNSIGNED NOT NULL,
    PRIMARY KEY (freelancer_id, skill_id),
    CONSTRAINT fk_fs_freelancer FOREIGN KEY (freelancer_id) REFERENCES freelancers(user_id) ON DELETE CASCADE,
    CONSTRAINT fk_fs_skill FOREIGN KEY (skill_id) REFERENCES skills(id) ON DELETE CASCADE,
    INDEX idx_fs_skill (skill_id)
);

CREATE TABLE projects (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    client_id INT UNSIGNED NOT NULL,
    title VARCHAR(200) NOT NULL,
    description TEXT,
    budget_min DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    budget_max DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    status ENUM('open', 'in_progress', 'completed', 'cancelled') NOT NULL DEFAULT 'open',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    CONSTRAINT fk_projects_client FOREIGN KEY (client_id) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_projects_status (status),
    INDEX idx_projects_client (client_id)
);

CREATE TABLE project_skills (
    project_id INT UNSIGNED NOT NULL,
    skill_id INT UNSIGNED NOT NULL,
    PRIMARY KEY (project_id, skill_id),
    CONSTRAINT fk_ps_project FOREIGN KEY (project_id) REFERENCES projects(id) ON DELETE CASCADE,
    CONSTRAINT fk_ps_skill FOREIGN KEY (skill_id) REFERENCES skills(id) ON DELETE CASCADE,
    INDEX idx_ps_skill (skill_id)
);

CREATE TABLE bids (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    project_id INT UNSIGNED NOT NULL,
    freelancer_id INT UNSIGNED NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    delivery_days SMALLINT UNSIGNED NOT NULL,
    cover_letter TEXT,
    status ENUM('pending', 'accepted', 'rejected') NOT NULL DEFAULT 'pending',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    CONSTRAINT fk_bids_project FOREIGN KEY (project_id) REFERENCES projects(id) ON DELETE CASCADE,
    CONSTRAINT fk_bids_freelancer FOREIGN KEY (freelancer_id) REFERENCES freelancers(user_id) ON DELETE CASCADE,
    UNIQUE KEY uq_bids_project_freelancer (project_id, freelancer_id),
    INDEX idx_bids_project (project_id),
    INDEX idx_bids_freelancer (freelancer_id)
);

CREATE TABLE contracts (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    bid_id INT UNSIGNED NOT NULL,
    agreed_amount DECIMAL(10,2) NOT NULL,
    start_date DATE NOT NULL,
    status ENUM('active', 'completed', 'cancelled') NOT NULL DEFAULT 'active',
    PRIMARY KEY (id),
    UNIQUE KEY uq_contracts_bid (bid_id),
    CONSTRAINT fk_contracts_bid FOREIGN KEY (bid_id) REFERENCES bids(id) ON DELETE RESTRICT
);

CREATE TABLE reviews (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    contract_id INT UNSIGNED NOT NULL,
    reviewer_id INT UNSIGNED NOT NULL,
    reviewee_id INT UNSIGNED NOT NULL,
    rating TINYINT UNSIGNED NOT NULL,
    comment TEXT,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    UNIQUE KEY uq_reviews_contract_reviewer (contract_id, reviewer_id),
    CONSTRAINT fk_reviews_contract FOREIGN KEY (contract_id) REFERENCES contracts(id) ON DELETE CASCADE,
    CONSTRAINT fk_reviews_reviewer FOREIGN KEY (reviewer_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_reviews_reviewee FOREIGN KEY (reviewee_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT chk_reviews_rating CHECK (rating BETWEEN 1 AND 5)
);

CREATE TABLE reports (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    reporter_id INT UNSIGNED NOT NULL,
    reported_user_id INT UNSIGNED NOT NULL,
    reason ENUM('spam', 'fake_review', 'inappropriate_content', 'payment_fraud', 'harassment', 'other') NOT NULL,
    details TEXT,
    project_id INT UNSIGNED DEFAULT NULL,
    review_id INT UNSIGNED DEFAULT NULL,
    status ENUM('pending', 'reviewed', 'dismissed') NOT NULL DEFAULT 'pending',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    CONSTRAINT fk_reports_reporter FOREIGN KEY (reporter_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_reports_reported FOREIGN KEY (reported_user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_reports_project FOREIGN KEY (project_id) REFERENCES projects(id) ON DELETE SET NULL,
    CONSTRAINT fk_reports_review FOREIGN KEY (review_id) REFERENCES reviews(id) ON DELETE SET NULL,
    INDEX idx_reports_status (status),
    INDEX idx_reports_reported (reported_user_id)
);

